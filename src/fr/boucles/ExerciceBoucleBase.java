package fr.boucles;

public class ExerciceBoucleBase {
    public static  void main(String[] args) {

//      On compte de 1 à 10
        for (int i = 1; i <= 10 ; i++) {
            System.out.println(i);
        }

//      On affiche IKHLEF KHALED 20 fois
        for (int i = 1; i <= 20 ; i++) {
            System.out.println("IKHLEF KHALED");
        }


//      On affiche les éléments pairs de 2 à 100
        for (int i = 2; i <= 100 ; i += 2) {
            System.out.println(i);
        }

//      On affiche les éléments pairs de 1 à 99
        for (int i = 1; i <= 99 ; i += 2) {
            System.out.println(i);
        }


    }
}
